@Composable
fun AppTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable () -> Unit
) {
    // 1. Determine if dynamic color is available AND should be used.
    // This logic now lives entirely within the theme.
    var useDynamicColor = Build.VERSION.SDK_INT >= Build.VERSION_CODES.S
    // 1a. if dev wants to force use of palette in Color.kt (disallow using dynamic colors) uncomment the following line.
    // useDynamicColor = false

    // 2. Check for high-contrast accessibility setting.
    // There is no reliable check for this setting so this check is disabled.
    /*
    val isHighContrastMode = (LocalContext.current.resources.configuration.uiMode and
            Configuration.UI_MODE_NIGHT_MASK) == Configuration.UI_MODE_NIGHT_NO &&
            (LocalContext.current.resources.configuration.fontScale > 1.0f) // A common proxy for this setting
    */

    val colorScheme = when {
        // Priority 1: Dynamic color, if available.
        useDynamicColor -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }
        // Priority 2: High-contrast mode, if enabled.
        /*
        isHighContrastMode -> {
            if (darkTheme) highContrastDarkColorScheme else highContrastLightColorScheme
        }*/

        // Priority 3: Standard dark theme.
        darkTheme -> darkScheme
        // Priority 4: Standard light theme (default fallback).
        else -> lightScheme
  }

  MaterialTheme(
    colorScheme = colorScheme,
    typography = AppTypography,
    content = content
  )
}
